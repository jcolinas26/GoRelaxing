public class VariableAtras {// controlar el boton atras del tlf

	static String activityenter;
	static int contadorenter;

	static long control;
	static int cont;

	public static String getDato() {

		return activityenter;

	}// end get

	public static void setDato(String s) {

		activityenter = s;

	}// end set

	public static int getContador() {

		return contadorenter;

	}// end get

	public static void setContador(int c) {

		contadorenter = c;

	}// end set

	// /////////////////

	public static long getcontrol() {

		return control;
	}// end get

	public static void setcontrol(long c) {

		control = c;
	}// end set

	// /////////////////

	public static int getcont() {

		return cont;
	}// end get

	public static void setcont(int cd) {

		cont = cd;
	}// end set

}// end variable atras
